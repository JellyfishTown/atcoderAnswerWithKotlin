package participated.abc115

fun main(args: Array<String>) {
    val i = readLine()!!.toInt()
    if (i == 25) {
        print("Christmas")
    } else if (i == 24) {
        print("Christmas Eve")

    } else if (i == 23) {
        print("Christmas Eve Eve")

    } else if (i == 22) {
        print("Christmas Eve Eve Eve")
    }
}
